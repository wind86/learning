class calculator:
    def sum(num1,num2):
        return num1 + num2
    
    def subt(num1,num2):
        return num1 - num2
    
    def mult(num1,num2):
        return num1 * num2
    
print(calculator.sum(1, 2))
print(calculator.mult(2, 3))
print(calculator.subt(5, 1))
